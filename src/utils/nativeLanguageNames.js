// Special case for Auto Detect
export const AUTO_DETECT_NATIVE = 'Auto Detect';

/**
 * Get the native name of a language
 * @param {string} englishName - The English name of the language
 * @returns {string} - The native name of the language
 */
export const getNativeLanguageName = (englishName) => {
  if (englishName === 'Auto Detect') return AUTO_DETECT_NATIVE;
  return NATIVE_LANGUAGE_NAMES[englishName] || englishName;
};

/**
 * Get the English name of a language from its native name
 * @param {string} nativeName - The native name of the language
 * @returns {string} - The English name of the language
 */
export const getEnglishLanguageName = (nativeName) => {
  if (nativeName === AUTO_DETECT_NATIVE) return 'Auto Detect';

  // Find the English name by looking up the native name
  for (const [english, native] of Object.entries(NATIVE_LANGUAGE_NAMES)) {
    if (native === nativeName) {
      return english;
    }
  }

  return nativeName; // Return as is if not found
};
